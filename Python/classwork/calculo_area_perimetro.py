"""" Escreva um programa que leia a base e a altura de um retângulo e exiba sua área e seu perímetro.
Fórmulas: Area = base * altura | Perimetro = 2 * (base + altura)."""

b = int(input("Digite a base do retângulo: "))
a = int(input("Digite a alutra do retângulo: "))

print("A area do retângulo é de: ", b * a)
print("O perímetro do retângulo é de: ", 2 * (b + a))